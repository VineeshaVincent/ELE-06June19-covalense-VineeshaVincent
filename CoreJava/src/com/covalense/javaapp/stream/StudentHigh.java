package com.covalense.javaapp.stream;

public class StudentHigh {

int tkno;
String name;
double percentage;
public int getTkno() {
	return tkno;
}


public String getName() {
	return name;
}


public double getPercentage() {
	return percentage;
}


public StudentHigh(int tkno, String name, double percentage) {
	super();
	this.tkno = tkno;
	this.name = name;
	this.percentage = percentage;
}

}



