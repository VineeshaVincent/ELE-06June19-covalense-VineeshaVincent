package com.covalense.javaapp.assignmenteight;

public class EmployeeSal {

	int id;
	String name;
	double salary;
	public int getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public double getSalary() {
		return salary;
	}
	public EmployeeSal(int id, String name, double salary) {
		super();
		this.id = id;
		this.name = name;
		this.salary = salary;
	}
	
	
}
