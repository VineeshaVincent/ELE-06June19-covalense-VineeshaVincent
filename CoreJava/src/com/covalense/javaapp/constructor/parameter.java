package com.covalense.javaapp.constructor;

public class parameter {
	
	parameter(int x)
	{
		System.out.println("it is a integer parameter constructor");
	}
	parameter(double x)
	{
		System.out.println("it is a double parameter constructor");
	}
	parameter()
	{
		System.out.println("it is constructor");
	}

	parameter(int x,int y)
	{
		System.out.println("it is a integer,integer parameter constructor");
	}
}
