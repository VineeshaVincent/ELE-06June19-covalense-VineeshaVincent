package com.covalense.javaapp.immutable;

public final class person {
	
	private final String name;
	private final int age;
	public person(String name, int age) {
		super();
		this.name = name;
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public int getAge() {
		return age;
	}
	
	
		
	}
	


